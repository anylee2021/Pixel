	D3D12_RESOURCE_DESC PixelBuffer::DescribeTex2D(uint32_t Width, uint32_t Height, uint32_t DepthOrArraySize, uint32_t NumMips, DXGI_FORMAT Format, UINT Flags)
	{
		m_Width = Width;
		m_Height = Height;
		m_ArraySize = DepthOrArraySize;
		m_Format = Format;

		D3D12_RESOURCE_DESC Desc = {};
		Desc.Alignment = 0;
		Desc.DepthOrArraySize = (uint16_t)DepthOrArraySize;
		Desc.Dimension = D3D12_RESOURCE_DIMENSION_TEXTURE2D;
		Desc.Flags = (D3D12_RESOURCE_FLAGS)Flags;
		Desc.Format = GetBaseFormat(Format);
		Desc.Height = (uint32_t)Height;
		Desc.Layout = D3D12_TEXTURE_LAYOUT_UNKNOWN;
		Desc.MipLevels = (uint16_t)NumMips;
		Desc.SampleDesc.Count = 1;
		Desc.SampleDesc.Quality = 0;
		Desc.Width = (uint64_t)Width;
		return Desc;
	}

	void PixelBuffer::AssociateWithResource(const std::wstring& Name, ID3D12Resource* pResource, D3D12_RESOURCE_STATES CurrentState)
	{
		PX_CORE_ASSERT(pResource != nullptr, "resource is nullptr");

		D3D12_RESOURCE_DESC ResourceDesc = pResource->GetDesc();

		m_pResource.Attach(pResource);
		m_UsageState = CurrentState;

		m_Width = (uint32_t)ResourceDesc.Width;
		m_Height = ResourceDesc.Height;
		m_ArraySize = ResourceDesc.DepthOrArraySize;
		m_Format = ResourceDesc.Format;

#ifdef PX_DEBUG
		m_pResource->SetName(Name.c_str());
#else
		Name;
#endif
	}

	void PixelBuffer::CreateTextureResource(const std::wstring& Name, const D3D12_RESOURCE_DESC& ResourceDesc, D3D12_CLEAR_VALUE ClearValue, D3D12_GPU_VIRTUAL_ADDRESS VideoMemoryPtr /*= -1*/)
	{
		//parent class's destroy
		Destroy();

		CD3DX12_HEAP_PROPERTIES HeapProps(D3D12_HEAP_TYPE_DEFAULT);
		PX_CORE_ASSERT(DirectXDevice::Get()->GetDevice()->CreateCommittedResource(
			&HeapProps, D3D12_HEAP_FLAG_NONE, &ResourceDesc, D3D12_RESOURCE_STATE_COMMON, &ClearValue, IID_PPV_ARGS(&m_pResource)
		) >= 0, "create texture resource error!");

		m_UsageState = D3D12_RESOURCE_STATE_COMMON;
		m_GpuVirtualAddress = 0;//D3D12_GPU_VIRTUAL_ADDRESS_NULL

#ifdef PX_DEBUG
		m_pResource->SetName(Name.c_str());
#else
		(Name);
#endif
	}

	DXGI_FORMAT PixelBuffer::GetBaseFormat(DXGI_FORMAT Format)
	{
		switch (Format)
		{
		//type less
		case DXGI_FORMAT_R8G8B8A8_UNORM:
		case DXGI_FORMAT_R8G8B8A8_UNORM_SRGB:
			return DXGI_FORMAT_R8G8B8A8_TYPELESS;

		case DXGI_FORMAT_B8G8R8A8_UNORM:
		case DXGI_FORMAT_B8G8R8A8_UNORM_SRGB:
			return DXGI_FORMAT_B8G8R8A8_TYPELESS;

		case DXGI_FORMAT_B8G8R8X8_UNORM:
		case DXGI_FORMAT_B8G8R8X8_UNORM_SRGB:
			return DXGI_FORMAT_B8G8R8X8_TYPELESS;
		
		//32-bit z w stencil
		//x : means unused channel
		case DXGI_FORMAT_R32G8X24_TYPELESS:
		case DXGI_FORMAT_D32_FLOAT_S8X24_UINT:
		case DXGI_FORMAT_R32_FLOAT_X8X24_TYPELESS:
		case DXGI_FORMAT_X32_TYPELESS_G8X24_UINT:
			return DXGI_FORMAT_R32G8X24_TYPELESS;

		//no stencil
		case DXGI_FORMAT_R32_TYPELESS:
		case DXGI_FORMAT_D32_FLOAT:
		case DXGI_FORMAT_R32_FLOAT:
			return DXGI_FORMAT_R32_TYPELESS;

		//24-bit Z
		case DXGI_FORMAT_R24G8_TYPELESS:
		case DXGI_FORMAT_D24_UNORM_S8_UINT:
		case DXGI_FORMAT_R24_UNORM_X8_TYPELESS:
		case DXGI_FORMAT_X24_TYPELESS_G8_UINT:
			return DXGI_FORMAT_R24G8_TYPELESS;

		//16-bit z w/o stencil
		case DXGI_FORMAT_R16_TYPELESS:
		case DXGI_FORMAT_D16_UNORM:
		case DXGI_FORMAT_R16_UNORM:
			return DXGI_FORMAT_R16_TYPELESS;

		default:
			return Format;
		}
	}

